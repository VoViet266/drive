void dodulieuvao_jtable(){
       DefaultTableModel model = (DefaultTableModel) Jtbl_qlcb.getModel();
       // xoa tat ca cac hang cua jtabel
       model.setNumRows(0);
        //DefaultTableModel model = (DefaultTableModel) Jtbl_qlcb.getModel();
       ketnoicsdl_mysql kn = new ketnoicsdl_mysql();
        Connection con=kn.cnn;
         try{
            Statement stmt=con.createStatement();  
            String sql="select * from canbo";
            //System.out.println(sql);
            ResultSet rs=stmt.executeQuery(sql);  
            // hiển thị từng dòng trong kết quả câu truy vấn trả về
            while(rs.next()){
                model.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8)});
            }
            kn.dongketnoi();
        }
         catch(Exception e){ 
            System.out.println(e);
        } 
    }
