private void Jtbl_qlcbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Jtbl_qlcbMouseClicked
        // TODO add your handling code here:
    
        if(evt.getClickCount() == 1){
            int row = Jtbl_qlcb.rowAtPoint(evt.getPoint()); // select a row
            String macb =Jtbl_qlcb.getModel().getValueAt(row, 0).toString();
            String hoten =Jtbl_qlcb.getModel().getValueAt(row, 1).toString();
            String gioitinh =Jtbl_qlcb.getModel().getValueAt(row, 2).toString();
            String diachi =Jtbl_qlcb.getModel().getValueAt(row, 3).toString();
            String tuoi =Jtbl_qlcb.getModel().getValueAt(row, 4).toString();
            String bac="";
            if(Jtbl_qlcb.getModel().getValueAt(row, 5)!=null){
                bac=Jtbl_qlcb.getModel().getValueAt(row, 5).toString();
            }
            String nganh="";
            if(Jtbl_qlcb.getModel().getValueAt(row, 6)!=null){
                nganh =Jtbl_qlcb.getModel().getValueAt(row, 6).toString();
            }
            String congviec="";
            if(Jtbl_qlcb.getModel().getValueAt(row, 7) !=null){
               congviec =Jtbl_qlcb.getModel().getValueAt(row, 7).toString();
            }
            
            txt_macb.setText(macb);
            txt_tencb.setText(hoten);
            cbx_gioitinh.setSelectedItem(gioitinh);
            txt_diachi.setText(diachi);
            int_tuoi.setText(tuoi);
            cbx_bac.setSelectedItem(bac);
//            txt_nganh.setText(nganh);
//            txt_congviec.setText(congviec);
           //qlcb ql =new qlcb();
           
            //dodulieuvao_jtable();
        }
        else{
            System.out.println("Vui lòng chọn dòng cần chinh sua");
        }
    }
